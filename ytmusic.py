from turtle import Turtle, Screen
import turtle
turtle.colormode(255)

t = Turtle()
s = Screen()
s.setup(height=600, width=800)
t.speed('slowest')
t.color('red')
t.pu()
t.goto(0, -90)
t.pd()
t.fillcolor('red')
t.begin_fill()
t.circle(120)
t.end_fill()

t.pu()
t.goto(0, -40)
t.pd()
t.pensize(5)
t.color('white')
t.circle(70)
t.pensize(1)
t.pu()
t.goto(0, -10)
t.setheading(180)
t.forward(25)
t.pu()
t.setheading(90)
t.pd()
t.fillcolor('white')
t.begin_fill()
for _ in range(3):
    t.forward(75)
    t.right(120)
t.end_fill()
t.ht()
s.exitonclick()
