from turtle import Turtle, Screen
import turtle
t = Turtle()
s = Screen()
turtle.colormode(255)
t.speed('slowest')
s.title("Indian Flag")
s.setup(height=600, width=900)
t.pu()

t.goto(x=0, y=100)
t.color((255, 103, 31))
t.pd()
t.begin_fill()
t.goto(x=450, y=100)
t.goto(x=450, y=300)
t.goto(x=-450, y=300)
t.goto(x=-450, y=100)
t.end_fill()
t.pu()
t.goto(x=0, y=0)

t.goto(x=0, y=-100)
t.color((4, 106, 56))
t.pd()
t.begin_fill()
t.goto(x=450, y=-100)
t.goto(x=450, y=-300)
t.goto(x=-450, y=-300)
t.goto(x=-450, y=-100)
t.end_fill()
t.pu()
t.goto(x=0, y=0)

t.pu()
t.forward(60)
t.speed(0)
t.left(90)
t.pendown()
t.pencolor((6, 3, 141))
for i in range(24):
    t.pencolor((6, 3, 141))
    t.begin_fill()
    t.circle(4.6)
    t.left(15)
    t.width(12)
    t.forward(15)
    t.width(0)
    t.end_fill()
t.penup()
t.setpos(0, 0)
t.backward(8)
t.pendown()
t.right(90)
t.forward(2)
for i in range(24):
    t.width(7)
    t.forward(10)
    t.width(2)
    t.forward(50)
    t.backward(60)
    t.left(15)
t.ht()
t.penup()
s.exitonclick()
